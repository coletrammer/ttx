static auto to_lower(c32 code_point) -> c32 {
    if (code_point >= U'A' && code_point <= U'Z') {
        return c32(code_point | 0x20);
    }
    return code_point;
}

auto Color::from_name(di::StringView color) -> di::Result<Color> {
    for (auto const& [n, c] : color_table) {
        if (di::container::equal(color | di::transform(to_lower), n)) {
            return c;
        }
    }
    return di::Unexpected(di::format_error("Faild to match '{}' to a known color constant"_sv, color));
}

auto Color::from_string(di::StringView color) -> di::Result<Color> {
    if (color.starts_with('#')) {
        if (color.size_bytes() != 7) {
            return di::Unexpected(di::format_error(
                "Hex color should have exactly 6 digits but got {} for color '{}'"_sv, color.size_bytes() - 1, color));
        }
        auto result = di::run_parser(di::parser::integer<u32, di::parser::IntegerMode::Improved>(16),
                                     color.substr(di::next(color.begin())));
        if (!result) {
            return di::Unexpected(di::format_error("Encountered unexpected hex digit for color '{}'"_sv, color));
        }
        auto value = result.value();
        return Color(u8(value >> 16), u8(value >> 8), u8(value));
    }
    if (color.starts_with("palette:"_sv)) {
        auto result = di::run_parser(di::parser::integer<u32, di::parser::IntegerMode::Improved>(16),
                                     color.substr(di::next(color.begin(), 8)));
        if (!result) {
            return di::Unexpected(
                di::format_error("Expected unsigned integer after 'palette:' for color '{}'"_sv, color));
        }
        if (result.value() >= u32(PaletteIndex::SpecialBegin)) {
            return di::Unexpected(di::format_error("Expected number in color '{}' to be less than 256"_sv, color));
        }
        return Color(Color::Palette(result.value()));
    }
    return Color::from_name(color);
}

auto Color::to_string() const -> di::String {
    if (is_default()) {
        return "default"_s;
    }
    if (is_dynamic()) {
        return "dynamic"_s;
    }
    if (is_palette()) {
        return di::format("palette:{}"_sv, u32(r));
    }
    return di::format("#{:02x}{:02x}{:02x}"_sv, r, g, b);
}

auto Color::brightness() const -> f64 {
    if (!is_custom()) {
        return 0;
    }

    // Use this algorithm: https://www.w3.org/TR/AERT/#color-contrast
    auto const red = r / 255.0;
    auto const green = g / 255.0;
    auto const blue = b / 255.0;
    return (red * 299 + green * 587 + blue * 114) / 1000;
}

auto Color::dimmed(u32 dim_factor) const -> Color {
    if (!is_custom()) {
        return *this;
    }

    auto scaled_factor = 255 - dim_factor * 255 / 100;
    return {
        u8(r * scaled_factor / 255),
        u8(g * scaled_factor / 255),
        u8(b * scaled_factor / 255),
    };
}
